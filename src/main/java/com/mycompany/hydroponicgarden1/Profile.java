    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        User user = session.getCurrentUser();
        if (user == null) {

    JOptionPane.showMessageDialog(
            this,
            "No hay una sesión activa.");

    return;
}

user.setFirstName(txtFirstName.getText().trim());
user.setLastName(txtLastName.getText().trim());
user.setEmailOrPhone(txtEmailOrPhone.getText().trim());

String birthDate = cmbYear.getSelectedItem() + "-"
        + cmbMonth.getSelectedItem() + "-"
        + cmbDay.getSelectedItem();

user.setBirthDate(java.sql.Date.valueOf(birthDate));

UserDAO userDAO = new UserDAO();

if (userDAO.updateUser(user)) {

    JOptionPane.showMessageDialog(
            this,
            "Datos actualizados correctamente.");

} else {

    JOptionPane.showMessageDialog(
            this,
            "No fue posible actualizar los datos.");
}
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
        // TODO add your handling code here:
        String newPassword = txtNewPassword.getText().trim();
String confirmPassword = txtConfirmPassword.getText().trim();

if (newPassword.isEmpty() || confirmPassword.isEmpty()) {

    JOptionPane.showMessageDialog(
            this,
            "Completa ambos campos.");

    return;
}

if (!newPassword.equals(confirmPassword)) {

    JOptionPane.showMessageDialog(
            this,
            "Las contraseñas no coinciden.");

    return;
}

User user = session.getCurrentUser();
if(user == null) {

    JOptionPane.showMessageDialog(
            this,
            "No hay una sesión activa.");

    return;
}

UserDAO userDAO = new UserDAO();

if (userDAO.updatePassword(user.getUserId(), newPassword)) {

    user.setPassword(newPassword);

    JOptionPane.showMessageDialog(
            this,
            "Contraseña actualizada correctamente.");

    txtNewPassword.setText("");
    txtConfirmPassword.setText("");

} else {

    JOptionPane.showMessageDialog(
            this,
            "No fue posible actualizar la contraseña.");
}
    }//GEN-LAST:event_btnChangePasswordActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(
        this,
        "¿Estás seguro de eliminar tu cuenta?\nEsta acción no se puede deshacer.",
        "Confirmar eliminación",
        JOptionPane.YES_NO_OPTION);

if (option == JOptionPane.YES_OPTION) {

    User user = session.getCurrentUser();
    if (user == null) {

    JOptionPane.showMessageDialog(
            this,
            "No hay una sesión activa.");

    return;
}

    UserDAO userDAO = new UserDAO();

    if (userDAO.deleteUser(user.getUserId())) {

        session.logout();

        JOptionPane.showMessageDialog(
                this,
                "La cuenta fue eliminada correctamente.");

        new Login().setVisible(true);

        dispose();

    } else {

        JOptionPane.showMessageDialog(
                this,
                "No fue posible eliminar la cuenta.");
    }
}
    }//GEN-LAST:event_btnDeleteActionPerformed

