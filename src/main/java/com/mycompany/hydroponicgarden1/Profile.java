    private void animarMenu() {

        Timer timer = new Timer(5, null);

        timer.addActionListener(e -> {

            if (!menuAbierto) {

                // Abrir menú
                if (menuPanel.getX() < 0) {

                    menuPanel.setLocation(
                            menuPanel.getX() + 10,
                            55);

                } else {

                    menuPanel.setLocation(0,55);
                    menuAbierto = true;
                    timer.stop();
                }

            } else {

                // Cerrar menú
                if (menuPanel.getX() > -anchoMenu) {

                    menuPanel.setLocation(
                            menuPanel.getX() - 10,
                            55);

                } else {

                    menuPanel.setLocation(-anchoMenu,55);
                    menuAbierto = false;
                    timer.stop();
                }
            }
        });

        timer.start();
    }
