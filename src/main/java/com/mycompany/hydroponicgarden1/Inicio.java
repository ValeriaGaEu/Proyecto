    private void animarMenu() {

        Timer timer = new Timer(5, null);

        timer.addActionListener(e -> {

            if (!menuAbierto) {

                // Abrir menú
                if (panelMenu.getX() < 0) {

                    panelMenu.setLocation(
                            panelMenu.getX() + 10,
                            55);

                } else {

                    panelMenu.setLocation(0,55);
                    menuAbierto = true;
                    timer.stop();
                }

            } else {

                // Cerrar menú
                if (panelMenu.getX() > -anchoMenu) {

                    panelMenu.setLocation(
                            panelMenu.getX() - 10,
                            55);

                } else {

                    panelMenu.setLocation(-anchoMenu,55);
                    menuAbierto = false;
                    timer.stop();
                }
            }
        });

        timer.start();
    }
